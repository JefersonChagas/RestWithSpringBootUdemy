package br.com.erudio.math;

public class SimpleMath {

	public Double sum(Double priNumero, Double segNumero) {
		return priNumero + segNumero;
	}

	public Double sub(Double priNumero, Double segNumero) {

		return priNumero - segNumero;

	}

	public Double mult(Double priNumero, Double segNumero) {

		return priNumero * segNumero;

	}

	public Double div(Double priNumero, Double segNumero) {

		return priNumero * segNumero;

	}

	public Double media(Double priNumero, Double segNumero) {

		return (priNumero + segNumero) / 2;

	}

	public Double raiz(Double numero) {

		return Math.sqrt(numero);

	}

}
